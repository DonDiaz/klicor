function asset(label, symbol, badge = "") {
  return { label, symbol, badge };
}

export function resolveCommerceCategoryAsset(iconKey = "", vertical = "") {
  const cleanKey = String(iconKey || "").trim().toLowerCase();
  const fallbackKey = VERTICAL_FALLBACKS[String(vertical || "").trim()] || COMMERCE_CATEGORY_ASSET_FALLBACK;
  const assetValue = COMMERCE_CATEGORY_ASSETS[cleanKey] || COMMERCE_CATEGORY_ASSETS[fallbackKey] || COMMERCE_CATEGORY_ASSETS[COMMERCE_CATEGORY_ASSET_FALLBACK];
  return {
    key: COMMERCE_CATEGORY_ASSETS[cleanKey] ? cleanKey : fallbackKey,
    ...assetValue,
  };
}
