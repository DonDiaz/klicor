export function resolveCommerceCategoryAsset(iconKey = "", vertical = "") {
  const cleanKey = String(iconKey || "").trim().toLowerCase();
  const hyphenKey = cleanKey.replace(/_/g, "-");
  const aliasKey = CATEGORY_ASSET_ALIASES[cleanKey] || CATEGORY_ASSET_ALIASES[hyphenKey] || "";
  const fallbackKey = VERTICAL_FALLBACKS[String(vertical || "").trim()] || COMMERCE_CATEGORY_ASSET_FALLBACK;
  const targetAsset = TARGET_COMMERCE_CATEGORY_ASSETS[cleanKey] || TARGET_COMMERCE_CATEGORY_ASSETS[hyphenKey] || null;
  const generatedAsset = resolveGeneratedAsset(cleanKey) || resolveGeneratedAsset(hyphenKey);
  const resolvedKey = COMMERCE_CATEGORY_ASSETS[cleanKey]
    ? cleanKey
    : COMMERCE_CATEGORY_ASSETS[hyphenKey]
      ? hyphenKey
      : COMMERCE_CATEGORY_ASSETS[aliasKey]
        ? aliasKey
        : generatedAsset
          ? cleanKey || hyphenKey
          : fallbackKey;
  const assetValue =
    targetAsset ||
    generatedAsset ||
    COMMERCE_CATEGORY_ASSETS[resolvedKey] ||
    COMMERCE_CATEGORY_ASSETS[COMMERCE_CATEGORY_ASSET_FALLBACK];
  return {
    key: targetAsset ? cleanKey : resolvedKey,
    ...assetValue,
  };
}
